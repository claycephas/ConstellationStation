
namespace ConstellationStation.Common
{
    /// <summary>
    /// Represents a set of spherical coordinates (phi and theta).
    /// </summary>
    public struct SphericalCoordinates
    {
        // Fields
        /// <summary>
        /// The phi portion of the coordinate, in radians.
        /// </summary>
        public float Phi;
        /// <summary>
        /// The theta portion of the coordinate, in radians.
        /// </summary>
        public float Theta;
    }
}
